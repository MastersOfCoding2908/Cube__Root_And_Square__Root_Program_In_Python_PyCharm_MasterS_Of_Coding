import math as m
cb_sq = int(input('Enter A Number To Get Square Root And Cube Root : '))
print(f'Square Root Of {cb_sq} = {m.sqrt(cb_sq)}')
print(f'Cube Root Of {cb_sq} = {m.cbrt(cb_sq)}')
